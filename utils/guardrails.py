# ─── 투자 권유 차단 문구 ────────────────────────────────────────────────────
_BLOCKED_PHRASES: list[str] = [
    "매수 추천", "지금 사세요", "반드시 오릅니다", "확실한 수익",
    "무조건 매수", "손실 없음", "보장된 수익",
    "사면 됩니다", "팔면 됩니다", "지금 팔아",
]

DISCLAIMER = "본 정보는 투자 판단을 위한 참고 자료이며, 투자 권유가 아닙니다."

# 정규식 객체 (모듈 로딩 시 1회 컴파일)
_RE_FINANCE = re.compile(
    "|".join(re.escape(kw) for kw in _FINANCE_TERMS),
    re.IGNORECASE,
)
_RE_COMPANY = re.compile(
    "|".join(re.escape(name) for name in _COMPANY_NAMES),
)  # 한글 회사명은 대소문자 구분 불필요 → 플래그 없음


# ─── 공개 함수 ───────────────────────────────────────────────────────────────
def is_in_scope(user_input: str) -> bool:
    """금융 키워드 또는 기업명이 포함된 질문이면 True를 반환한다."""
    return bool(_RE_FINANCE.search(user_input)) or bool(_RE_COMPANY.search(user_input))


def validate_scope(user_input: str) -> tuple[bool, str]:
    """
    범위 체크 결과와 거절 메시지를 함께 반환한다.

    Returns:
        (True, "")           — 허용
        (False, rejection)   — 차단 (재테크 외 질문)
    """
    if is_in_scope(user_input):
        return True, ""
    rejection = (
        "죄송합니다. 저는 주식·재테크 관련 질문만 답변할 수 있습니다. "
        "종목 분석, 포트폴리오, 시장 동향 등에 대해 질문해 주세요."
    )
    return False, rejection


def filter_investment_advice(text: str) -> str:
    """투자 권유성 문구를 중립 표현으로 교체한다."""
    for phrase in _BLOCKED_PHRASES:
        text = text.replace(phrase, "[참고 정보]")
    return text


def ensure_disclaimer(text: str) -> str:
    """리포트 말미에 면책 문구가 없으면 추가한다."""
    if DISCLAIMER not in text:
        text = text.rstrip() + f"\n\n---\n{DISCLAIMER}"
    return text
