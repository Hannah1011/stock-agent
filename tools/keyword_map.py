def get_keywords_for_ticker(ticker: str) -> list[str]:
    """
    ticker에 매핑된 뉴스 검색 키워드를 반환한다.
    미등록 종목은 ticker에서 코드 부분만 빼고 반환한다.
    """
    if ticker in TICKER_KEYWORD_MAP:
        return TICKER_KEYWORD_MAP[ticker]
    # 미등록 종목: stock_resolver의 캐시에서 이름을 꺼내 단순 검색
    bare = ticker.replace(".KS", "").replace(".KQ", "")
    return [bare]


def add_ticker_keywords(ticker: str, keywords: list[str]) -> None:
    """런타임에 특정 종목의 뉴스 키워드를 추가·덮어쓴다."""
    TICKER_KEYWORD_MAP[ticker] = keywords
