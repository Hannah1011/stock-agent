def get_keywords_for_ticker(ticker: str) -> list[str]:
    """종목 ticker로 관련 키워드를 가져온다. 미등록 종목은 ticker 자체를 반환."""
    return TICKER_KEYWORD_MAP.get(ticker, [ticker.replace(".KS", "").replace(".KQ", "")])


def get_sector(ticker: str) -> Optional[str]:
    """ticker의 섹터를 반환한다. 미등록이면 None."""
    return TICKER_SECTOR_MAP.get(ticker)


def add_ticker(ticker: str, keywords: list[str], sector: Optional[str] = None) -> None:
    """런타임에 새 종목 키워드 맵을 추가한다."""
    TICKER_KEYWORD_MAP[ticker] = keywords
    if sector:
        TICKER_SECTOR_MAP[ticker] = sector
